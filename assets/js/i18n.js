let currentLang = localStorage.getItem("tween_lang")
  || (navigator.language?.startsWith("ja") ? "ja" : "en");

function t(key, ...args) {
  const val = (I18N[currentLang] ?? I18N.en)[key] ?? I18N.en[key] ?? key;
  return typeof val === "function" ? val(...args) : val;
}

function applyLang(lang) {
  currentLang = lang;
  localStorage.setItem("tween_lang", lang);
  document.documentElement.lang = lang;
  document.querySelectorAll("[data-i18n]").forEach(el => {
    el.textContent = t(el.dataset.i18n);
  });
  document.querySelectorAll("[data-i18n-html]").forEach(el => {
    el.innerHTML = t(el.dataset.i18nHtml);
  });
  // Status bar: only update if showing a known stable text
  const readyTexts = [I18N.ja["status.ready"], I18N.en["status.ready"],
                      I18N.ja["status.loading"], I18N.en["status.loading"]];
  if (readyTexts.includes(dom.statusBar.textContent)) setStatus(t("status.ready"));
  // Tray lang toggle: show the OTHER language as the switch target
  const trayLang = document.getElementById("tray-lang");
  if (trayLang) trayLang.textContent = lang === "ja" ? "EN" : "JP";
}

function switchLang(lang) {
  if (!lang) lang = currentLang === "ja" ? "en" : "ja";
  applyLang(lang);
}
